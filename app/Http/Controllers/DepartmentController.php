     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        # Get departments
        $departments = Department::paginate(20);

        # Check if last page is reached
        if($departments->currentPage() > $departments->lastPage()) {
            return response()->json([
                'success' => false,
                "message" => "You've reached the last page, LAST PAGE: " . $departments->lastPage()
            ]);
        }

        # Return departments
        return response()->json([
            'success' => true,
            'numOfResults' => $departments->count(),
            'lastPage' => $departments->lastPage(),
            'page' => $departments->currentPage(),
            'data' => DepartmentResource::collection($departments)
        ]);
    }

